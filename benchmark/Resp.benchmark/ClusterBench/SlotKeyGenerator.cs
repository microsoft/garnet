using System.Text;
using Garnet.common;

namespace Resp.benchmark
{
    /// <summary>
    /// Generates keys that route to a specific shard using Redis hash-tag mechanism.
    /// Uses a 2-char printable ASCII tag (3-char fallback) for slot routing, and
    /// base-62 encoded suffix for maximum key space density.
    /// </summary>
    public class SlotKeyGenerator
    {
        static readonly char[] Base62Chars = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz".ToCharArray();

        /// <summary>
        /// A hash-tag string whose CRC16 mod 16384 lands in one of the shard's assigned slots.
        /// </summary>
        private readonly string tag;

        /// <summary>
        /// Key length (total bytes including hash-tag wrapper).
        /// </summary>
        private readonly int keyLen;

        /// <summary>
        /// The prefix "{tag}" that all generated keys start with.
        /// </summary>
        private readonly string keyPrefix;

        /// <summary>
        /// Number of suffix characters available after the prefix.
        /// </summary>
        private readonly int suffixLen;

        /// <summary>
        /// The key prefix used for all keys generated by this instance.
        /// </summary>
        public string KeyPrefix => keyPrefix;

        public SlotKeyGenerator(PrimaryInfo shard, int keyLen)
        {
            this.keyLen = Math.Max(keyLen, 8);
            this.tag = FindTagForShard(shard);
            this.keyPrefix = $"{{{tag}}}";
            this.suffixLen = this.keyLen - keyPrefix.Length;
        }

        /// <summary>
        /// Generate a key that routes to this shard.
        /// Format: {tag} + base-62 encoded keyIndex, padded to keyLen.
        /// </summary>
        public string GenerateKey(Random rng, int keyIndex = -1)
        {
            var idx = keyIndex >= 0 ? keyIndex : rng.Next(int.MaxValue);
            var sb = new StringBuilder(keyLen);
            sb.Append(keyPrefix);
            AppendBase62(sb, idx, suffixLen);
            return sb.ToString();
        }

        /// <summary>
        /// Encode value as base-62 string, left-padded with '0' to exactly the given width.
        /// </summary>
        private static void AppendBase62(StringBuilder sb, int value, int width)
        {
            Span<char> buf = stackalloc char[width];
            for (int i = width - 1; i >= 0; i--)
            {
                buf[i] = Base62Chars[value % 62];
                value /= 62;
            }
            sb.Append(buf);
        }

        /// <summary>
        /// Find a 2-char printable ASCII tag that hashes to any slot owned by this shard.
        /// Uses all printable ASCII (33-126) excluding '{' and '}'. 8,464 combos covers any
        /// realistic shard. Falls back to 3-char if needed.
        /// </summary>
        private static unsafe string FindTagForShard(PrimaryInfo shard)
        {
            byte* buf = stackalloc byte[3];

            // Try 2-char tags (92² = 8,464 combos)
            for (byte c1 = 33; c1 < 127; c1++)
            {
                if (c1 is (byte)'{' or (byte)'}') continue;
                buf[0] = c1;
                for (byte c2 = 33; c2 < 127; c2++)
                {
                    if (c2 is (byte)'{' or (byte)'}') continue;
                    buf[1] = c2;
                    if (shard.OwnsSlot(HashSlotUtils.HashSlot(buf, 2)))
                        return $"{(char)c1}{(char)c2}";
                }
            }

            // Fallback: 3-char tags (92³ = 778,688 combos — guaranteed)
            for (byte c1 = 33; c1 < 127; c1++)
            {
                if (c1 is (byte)'{' or (byte)'}') continue;
                buf[0] = c1;
                for (byte c2 = 33; c2 < 127; c2++)
                {
                    if (c2 is (byte)'{' or (byte)'}') continue;
                    buf[1] = c2;
                    for (byte c3 = 33; c3 < 127; c3++)
                    {
                        if (c3 is (byte)'{' or (byte)'}') continue;
                        buf[2] = c3;
                        if (shard.OwnsSlot(HashSlotUtils.HashSlot(buf, 3)))
                            return $"{(char)c1}{(char)c2}{(char)c3}";
                    }
                }
            }

            throw new Exception($"Failed to find a hash-tag for shard {shard}");
        }
    }
}
